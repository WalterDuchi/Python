# ejemplo de inicialización de todos los tipos de elementos en python

# Inicializar variables
# Enteros
a = 1
print("a=", a, type(a))
# Flotantes
b = 1.5
print("b=", b, type(b))
# Cadenas
c = "Hola Mundo"
print("c=", c, type(c))
# Listas
d = [1, 2, 3, 4, 5]
print("d=", d, type(d))
# Tuplas
e = (1, 2, 3, 4, 5)
print("e=", e, type(e))
# Diccionarios
f = {"a": 1, "b": 2, "c": 3}
print("f=", f, type(f))
# Arreglos
g = [1, 2, 3, 4, 5]
print("g=", g, type(g))
# Dataframes
h = {"a": [1, 2, 3, 4, 5], "b": [6, 7, 8, 9, 10]}
print("h=", h, type(h))
# Gráficos
i = ([1, 2, 3, 4, 5], [1, 4, 9, 16, 25])
print("i=", i, type(i))
# Mapas de calor
j = h
print("j=", j, type(j))

# Imprimir tipos de variables
print("\nTipos de variables")
print(type(a))
print(type(b))
print(type(c))
print(type(d))
print(type(e))
print(type(f))
print(type(g))
print(type(h))
print(type(i))
print(type(j))
